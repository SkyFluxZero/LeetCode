class Solution {
public:
	string parseTernary(string expression) {
		size_t n = expression.size(), i = 0;
		while (i < n and expression.at(i) != '?') {
			i++;
		}
		if (i == n) {
			return expression;
		}
		size_t j = i + 1, k = 1;
		while (j < n and k > 0) {
			if (expression.at(j) == '?') {
				k++;
			}
			else if (expression.at(j) == ':') {
				k--;
			}
			j++;
		}
		if (expression.front() == 'T') {
			return parseTernary(expression.substr(2, j - 3));
		}
		return parseTernary(expression.substr(j));
	}
};

int main(void) {
	Solution solution;
	string expression, result, answer;

	expression = "T?2:3";
	answer = "2";
	result = solution.parseTernary(expression);
	assert(answer == result);

	expression = "F?1:T?4:5";
	answer = "4";
	result = solution.parseTernary(expression);
	assert(answer == result);

	expression = "T?T?F:5:3";
	answer = "F";
	result = solution.parseTernary(expression);
	assert(answer == result);

	cout << "\nPassed All\n";
	return 0;
}
