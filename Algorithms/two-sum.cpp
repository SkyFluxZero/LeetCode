// BEGIN: Time Complexity: Linear on nums.size(), Space Complexity: Linear on nums.size()
class Solution {
public:
	vector<int> twoSum(vector<int>& nums, int target) {
		unordered_map<int, int> h;
		for (int i = 0, n = nums.size(); i < n; i++) {
			if (!h.empty() and h.count(target - nums.at(i))) {
				return {h.at(target - nums.at(i)), i};
			}
			h[nums.at(i)] = i;
		}
		return {};
	}
};
// END: Time Linear on nums.size(), Space Complexity: Linear on nums.size()

int main(void) {
	Solution solution;
	vector<int> nums, result, answer;
	int target = 0;

	nums = {2, 7, 11, 15};
	target = 9;
	answer = {0, 1};
	result = solution.twoSum(nums, target);
	assert(answer == result);

	cout << "\nPassed All\n";
	return 0;
}