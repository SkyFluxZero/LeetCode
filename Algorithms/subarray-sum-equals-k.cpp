class Solution {
public:
	int subarraySum(vector<int>& nums, int k) {
		unordered_multiset<int> s;
		s.insert(0);
		int result = 0, sum = 0;
		for (const auto &i : nums) {
			sum += i;
			result += s.count(sum - k);
			s.insert(sum);
		}
		return result;
	}
};

int main(void) {
	Solution solution;
	vector<int> nums;
	int k = 0, result = 0, answer = 0;

	nums = {1, 1, 1};
	k = 2;
	answer = 2;
	result = solution.subarraySum(nums, k);
	assert(answer == result);

	cout << "\nPassed All\n";
	return 0;
}