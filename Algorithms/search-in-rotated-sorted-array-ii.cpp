class Solution {
public:
	bool search(const vector<int>& nums, const int& target) {
		if (nums.empty()) {
			return false;
		}
		if (nums.size() == 1) {
			return nums.front() == target;
		}
		int left = 0;
		int right = nums.size() - 1;
		int mid = 0;
		while (left <= right) {
			mid = left + (right - left) / 2;
			if (nums[mid] == target) {
				return true;
			}
			if (mid >= 1 && nums[mid - 1] == target) {
				return true;
			}
			if (mid + 1 < nums.size() && nums[mid + 1] == target) {
				return true;
			}
			if (nums[mid] > nums.front()) {
				if (nums.front() <= target && target < nums[mid]) {
					right = mid - 1;
				}
				else {
					left = mid + 1;
					while (left + 1 < nums.size() && nums[left] == nums[left + 1]) {
						++left;
					}
				}
			}
			else if (nums[mid] == nums.front()) {
				++left;
			}
			else {
				if (nums[mid] < target && target <= nums.back()) {
					left = mid + 1;
				}
				else {
					right = mid - 1;
					while (right >= 1 && nums[right] == nums[right - 1]) {
						--right;
					}
				}
			}
		}
		return nums[mid] == target;
	}
};
